const fareMap = new Map<string, number>()

for (const [from, to, fare] of fareData) {
  fareMap.set(`${from}-${to}`, fare)
  fareMap.set(`${to}-${from}`, fare)
}

export function getFare(origin: string, destination: string): number | null {
  if (origin === destination)
    return 0
  return fareMap.get(`${origin}-${destination}`) ?? null
}
